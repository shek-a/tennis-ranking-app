import { table, attribute, autoGeneratedHashKey } from '@aws/dynamodb-data-mapper-annotations';
import { PLAYER_TOURNAMENT_RESULTS_TABLE_NAME } from '@/constants';

@table(PLAYER_TOURNAMENT_RESULTS_TABLE_NAME)
export default class PlayerResult {
    @autoGeneratedHashKey()
    id!: string;

    @attribute()
    firstName!: string;

    @attribute()
    lastName!: string;

    @attribute()
    dateOfBirth!: Date;

    @attribute()
    tournament!: string;

    @attribute()
    points!: number;
}
